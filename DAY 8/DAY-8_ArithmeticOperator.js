var Exponentiation=5**2;  //same as pow.
document.write("<br/>Sum:",sum+
"<br/>Mul:",mul+
"<br/>Sub:",sub+
"<br/>Div:",div+
"<br/>Modulo:",mod+
"<br/>Post Inc:",n1++ +
"<br/>Post Dec:",n1-- +
"<br/>Pre Inc:",++n1+
"<br/>Pre Dec:",--n1+
"<br/>Exp:",Exponentiation);
